                   <h3>Capital Social</h3>
                   <div class="quote">
                        <blockquote>
                           <p>
                              "El capital social puede ser entendido como un recurso intangible, que permite a personas y grupos la obtención de beneficios por medio de relaciones sociales dotadas de confianza, reciprocidad y cooperación. Activos que se tienen como consecuencia de las relaciones sociales de una persona con otros y [de forma correlacionada] la participación en organizaciones. Tales relaciones facilitan el acceso a otros recursos."
